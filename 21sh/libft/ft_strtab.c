#include "libft.h"

int		ft_strtab(char **tab, char *str)
{
	int		i;

	i = 0;
	while (tab[i])
	{
		if (ft_strcmp(tab[i], str) == 0)
			return (i);
		i++;
	}
	return (-1);
}

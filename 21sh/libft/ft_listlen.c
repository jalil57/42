#include "libft.h"

int		ft_listlen(t_list **list)
{
	int		i;
	t_list	*tmp;

	i = 0;
	tmp = *list;
	while (tmp->next)
	{
		i++;
		tmp = tmp->next;
	}
	return (i);
}
